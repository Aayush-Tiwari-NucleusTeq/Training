package Day2.Final_Keyword;

public class FinalVariable {
    public static void main(String[] args) {
        System.out.println("Program illustrating final variable");
        final double i = 3.14;
        // i = 900.00; // We cannot update the value of i since its final variable
        System.out.println("The value of pi is -> " + i);

    }
}
