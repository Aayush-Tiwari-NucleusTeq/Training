public class Question_5 {
    public static void main(String[] args) {
        System.out.println("Program demonstrating Bitwise and Shift operators");
        System.out.println("Bitwise OR operator is - " + (12 | 25));
        System.out.println("Bitwise AND operator is - " + (12 & 25));
        System.out.println("Bitwise XOR operator is - " + (12 ^ 25));
        System.out.println("1's complement of 35 is - " + (~35));
        System.out.println("2's complement of 35 is - " + (~~35+1));
    }
}
