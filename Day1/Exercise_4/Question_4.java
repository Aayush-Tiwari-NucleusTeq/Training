public class Question_4 {
    public static void main(String[] args) {
        System.out.println("Program demonstrating increment and decrement operator in java");
        int i = 9;
        System.out.println("The value of i is -> " + i);
        System.out.println("The value of i after postfix increment is -> " + i++);
        System.out.println("The value of i after prefix increment is -> " + ++i);
        System.out.println();
        int j = 9;
        System.out.println("The value of j is -> " + j);
        System.out.println("The value of j after postfix decrement is -> " + j--);
        System.out.println("The value of i after prefix decrement is -> " + --j);
    }
}
