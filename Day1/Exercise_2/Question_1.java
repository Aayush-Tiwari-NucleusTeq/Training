public class Question_1 {
    public static void main(String[] args) {
        System.out.println("Primitive data types");
        
        byte b = 24;
		System.out.println("The byte type value is " + b);
		short s = 24;
		System.out.println("The short type value is " + s);
		int i = 24;
		System.out.println("The integer type value is " + i);
		long l = 23L;
		System.out.println("The long type value is " + l);
		float f = 24.0024F;
		System.out.println("The floating type value is " + f);
		double d = 24.24;
		System.out.println("The double type value is " + d);
		char c = 'A';
		System.out.println("The char type value is " + c);
		boolean bool = true;
		System.out.println("The boolean type value is " + bool);
    }
}
